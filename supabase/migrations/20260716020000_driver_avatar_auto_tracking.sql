create or replace function public.driver_avatar_tracking_health(p_email text default 'driver@daynightae.com')
returns jsonb
language sql
security definer
stable
set search_path=public,auth,storage
as $dn$
  select jsonb_build_object(
    'ok',
      exists(select 1 from auth.users where lower(email)=lower(p_email))
      and exists(
        select 1
        from public.driver_profiles dp
        join auth.users au on au.id=coalesce(dp.user_id,dp.id)
        where lower(au.email)=lower(p_email)
      )
      and to_regprocedure('public.driver_report_location(double precision,double precision,double precision,double precision,double precision,double precision,text,numeric,text)') is not null
      and to_regprocedure('public.driver_update_own_profile(text,text,text,text,text,text,text)') is not null
      and to_regprocedure('public.admin_update_driver_profile_v2(uuid,text,text,text,text,text,text,text,text,text,text,text,text,text,text,text,text,text,text)') is not null,
    'auth_user',exists(select 1 from auth.users where lower(email)=lower(p_email)),
    'driver_profile',exists(
      select 1 from public.driver_profiles dp join auth.users au on au.id=coalesce(dp.user_id,dp.id)
      where lower(au.email)=lower(p_email)
    ),
    'avatar_bucket',exists(select 1 from storage.buckets b where b.id='avatars'),
    'avatar_private',coalesce((select not b.public from storage.buckets b where b.id='avatars'),false),
    'avatar_column',exists(
      select 1 from information_schema.columns
      where table_schema='public' and table_name='driver_profiles' and column_name='avatar_path'
    ),
    'location_rpc',to_regprocedure('public.driver_report_location(double precision,double precision,double precision,double precision,double precision,double precision,text,numeric,text)') is not null,
    'self_profile_rpc',to_regprocedure('public.driver_update_own_profile(text,text,text,text,text,text,text)') is not null,
    'admin_profile_rpc',to_regprocedure('public.admin_update_driver_profile_v2(uuid,text,text,text,text,text,text,text,text,text,text,text,text,text,text,text,text,text,text)') is not null
  )
$dn$;

grant execute on function public.driver_avatar_tracking_health(text) to authenticated;

select public.driver_avatar_tracking_health('driver@daynightae.com');
