-- -----------------------------------------------------------------------------
-- 3) Driver self-service profile RPC.
-- -----------------------------------------------------------------------------

create or replace function public.driver_update_own_profile(
  p_phone text default null,
  p_emergency_contact text default null,
  p_avatar_path text default null,
  p_bio text default null,
  p_work_area text default null,
  p_address text default null,
  p_preferred_language text default 'ar'
)
returns jsonb
language plpgsql
security definer
set search_path=public,auth
as $dn$
declare
  v_driver public.driver_profiles%rowtype;
  v_avatar_owner text;
begin
  if auth.uid() is null then raise exception 'not_authenticated'; end if;

  select * into v_driver
  from public.driver_profiles
  where id=auth.uid() or user_id=auth.uid()
  order by case when id=auth.uid() then 0 else 1 end, created_at desc nulls last
  limit 1;

  if not found then raise exception 'driver_setup_required'; end if;
  if v_driver.status::text <> 'active' then raise exception 'driver_inactive'; end if;

  if p_avatar_path is not null then
    v_avatar_owner := split_part(p_avatar_path,'/',1);
    if v_avatar_owner <> auth.uid()::text then
      raise exception 'invalid_avatar_owner';
    end if;
    if not exists(
      select 1 from storage.objects
      where bucket_id='avatars' and name=p_avatar_path
    ) then
      raise exception 'avatar_object_not_found';
    end if;
  end if;

  update public.driver_profiles
  set phone=nullif(btrim(coalesce(p_phone,'')),''),
      emergency_contact=nullif(btrim(coalesce(p_emergency_contact,'')),''),
      avatar_path=coalesce(p_avatar_path,avatar_path),
      avatar_updated_at=case when p_avatar_path is not null then now() else avatar_updated_at end,
      bio=nullif(btrim(coalesce(p_bio,'')),''),
      work_area=nullif(btrim(coalesce(p_work_area,'')),''),
      address=nullif(btrim(coalesce(p_address,'')),''),
      preferred_language=case
        when lower(coalesce(p_preferred_language,'ar')) in ('ar','en')
          then lower(coalesce(p_preferred_language,'ar'))
        else 'ar'
      end,
      updated_at=now()
  where id=v_driver.id
  returning * into v_driver;

  update public.profiles
  set phone=v_driver.phone,updated_at=now()
  where id=auth.uid();

  perform public.driver_audit(
    v_driver.id,
    'driver_self_profile_updated',
    null,
    jsonb_build_object(
      'avatar_updated',p_avatar_path is not null,
      'work_area',v_driver.work_area,
      'preferred_language',v_driver.preferred_language
    )
  );

  return jsonb_build_object('ok',true,'driver',to_jsonb(v_driver));
end
$dn$;

