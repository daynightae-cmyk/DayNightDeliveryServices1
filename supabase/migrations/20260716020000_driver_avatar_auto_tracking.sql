create or replace function public.admin_update_driver_profile_v2(
  p_driver_id uuid,
  p_full_name text,
  p_phone text default null,
  p_status text default 'active',
  p_shift_status text default 'offline',
  p_vehicle_type text default null,
  p_vehicle_plate text default null,
  p_vehicle_color text default null,
  p_emirate text default null,
  p_license_number text default null,
  p_emergency_contact text default null,
  p_avatar_path text default null,
  p_bio text default null,
  p_work_area text default null,
  p_nationality text default null,
  p_address text default null,
  p_license_expiry text default null,
  p_vehicle_registration_expiry text default null,
  p_note text default null
)
returns jsonb
language plpgsql
security definer
set search_path=public,auth
as $dn$
declare
  v_driver public.driver_profiles%rowtype;
begin
  if not public.driver_is_admin() then raise exception 'not_authorized'; end if;
  if p_status not in ('active','inactive','suspended') then raise exception 'invalid_driver_status'; end if;
  if p_shift_status not in ('offline','available','busy','paused') then raise exception 'invalid_shift_status'; end if;

  if p_avatar_path is not null and not exists(
    select 1 from storage.objects where bucket_id='avatars' and name=p_avatar_path
  ) then
    raise exception 'avatar_object_not_found';
  end if;

  update public.driver_profiles
  set full_name=coalesce(nullif(btrim(coalesce(p_full_name,'')),''),full_name),
      phone=nullif(btrim(coalesce(p_phone,'')),''),
      status=p_status,
      shift_status=case when p_status='active' then p_shift_status else 'offline' end,
      vehicle_type=nullif(btrim(coalesce(p_vehicle_type,'')),''),
      vehicle_plate=nullif(btrim(coalesce(p_vehicle_plate,'')),''),
      vehicle_color=nullif(btrim(coalesce(p_vehicle_color,'')),''),
      emirate=nullif(btrim(coalesce(p_emirate,'')),''),
      license_number=nullif(btrim(coalesce(p_license_number,'')),''),
      emergency_contact=nullif(btrim(coalesce(p_emergency_contact,'')),''),
      avatar_path=coalesce(p_avatar_path,avatar_path),
      avatar_updated_at=case when p_avatar_path is not null then now() else avatar_updated_at end,
      bio=nullif(btrim(coalesce(p_bio,'')),''),
      work_area=nullif(btrim(coalesce(p_work_area,'')),''),
      nationality=nullif(btrim(coalesce(p_nationality,'')),''),
      address=nullif(btrim(coalesce(p_address,'')),''),
      license_expiry=nullif(btrim(coalesce(p_license_expiry,'')),'')::date,
      vehicle_registration_expiry=nullif(btrim(coalesce(p_vehicle_registration_expiry,'')),'')::date,
      last_status_note=nullif(btrim(coalesce(p_note,'')),''),
      updated_at=now()
  where id=p_driver_id
  returning * into v_driver;

  if not found then raise exception 'driver_not_found'; end if;

  update public.profiles
  set full_name=v_driver.full_name,
      phone=v_driver.phone,
      is_active=(v_driver.status::text='active'),
      updated_at=now()
  where id=coalesce(v_driver.user_id,v_driver.id);

  if v_driver.status::text<>'active' or v_driver.shift_status::text='offline' then
    update public.driver_locations
    set is_online=false,updated_at=now()
    where driver_id=v_driver.id;
  end if;

  perform public.driver_audit(
    v_driver.id,
    'admin_driver_profile_v2_updated',
    null,
    jsonb_build_object(
      'status',v_driver.status::text,
      'shift_status',v_driver.shift_status::text,
      'avatar_updated',p_avatar_path is not null,
      'note',p_note
    )
  );

  return jsonb_build_object('ok',true,'driver',to_jsonb(v_driver));
end
$dn$;

